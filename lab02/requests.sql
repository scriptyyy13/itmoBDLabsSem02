-- Запрос №6
-- Получить список студентов, отчисленных до первого сентября 2012 года с очной
--  или заочной формы обучения (специальность: Программная инженерия).
-- В результат включить:
-- номер группы;
-- номер, фамилию, имя и отчество студента;
-- номер пункта приказа;
-- Для реализации использовать подзапрос с EXISTS.
SELECT "Н_УЧЕНИКИ"."ГРУППА", "Н_ЛЮДИ"."ИД", "Н_ЛЮДИ"."ИМЯ", "Н_ЛЮДИ"."ФАМИЛИЯ", "Н_ЛЮДИ"."ОТЧЕСТВО", "Н_УЧЕНИКИ"."КОНЕЦ_ПО_ПРИКАЗУ"
FROM "Н_УЧЕНИКИ"
JOIN "Н_ЛЮДИ" ON "Н_УЧЕНИКИ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД"
WHERE "Н_УЧЕНИКИ"."СОСТОЯНИЕ" = 'отчислен'
  AND "Н_УЧЕНИКИ"."КОНЕЦ" < '2012-09-01'
  AND EXISTS (
      SELECT 1 FROM "Н_ПЛАНЫ"
      JOIN "Н_НАПР_СПЕЦ" ON "Н_ПЛАНЫ"."НАПС_ИД" = "Н_НАПР_СПЕЦ"."ИД"
      WHERE "Н_ПЛАНЫ"."ИД" = "Н_УЧЕНИКИ"."ПЛАН_ИД"
        AND "Н_НАПР_СПЕЦ"."НАИМЕНОВАНИЕ" ILIKE '%Программная инженерия%'
  );


