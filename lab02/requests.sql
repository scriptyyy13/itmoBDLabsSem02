-- Запрос №5
-- Выведите таблицу со средними оценками студентов группы 4100 (Номер, ФИО, Ср_оценка),
-- у которых средняя оценка не больше минимальной оценк(е|и) в группе 1100.
SELECT
    "Н_ВЕДОМОСТИ"."ЧЛВК_ИД",
    "Н_ЛЮДИ"."ФАМИЛИЯ",
    AVG("Н_ВЕДОМОСТИ"."ОЦЕНКА"::numeric)
FROM "Н_ВЕДОМОСТИ"
JOIN "Н_ЛЮДИ" ON "Н_ВЕДОМОСТИ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД"
WHERE "Н_ВЕДОМОСТИ"."ОЦЕНКА" IN ('2','3','4','5')
  AND "Н_ВЕДОМОСТИ"."ЧЛВК_ИД" IN (
      SELECT "Н_УЧЕНИКИ"."ЧЛВК_ИД"
      FROM "Н_УЧЕНИКИ"
      WHERE "Н_УЧЕНИКИ"."ГРУППА" = '4100'
  )
GROUP BY "Н_ВЕДОМОСТИ"."ЧЛВК_ИД", "Н_ЛЮДИ"."ФАМИЛИЯ"
HAVING AVG("Н_ВЕДОМОСТИ"."ОЦЕНКА"::numeric) <= (
    SELECT MIN("Н_ВЕДОМОСТИ"."ОЦЕНКА"::numeric)
    FROM "Н_ВЕДОМОСТИ"
    WHERE "Н_ВЕДОМОСТИ"."ОЦЕНКА" IN ('2','3','4','5')
      AND "Н_ВЕДОМОСТИ"."ЧЛВК_ИД" IN (
          SELECT "Н_УЧЕНИКИ"."ЧЛВК_ИД"
          FROM "Н_УЧЕНИКИ"
          WHERE "Н_УЧЕНИКИ"."ГРУППА" = '1100'
      )
);


