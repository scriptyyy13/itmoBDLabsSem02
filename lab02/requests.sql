-- Запрос №7
-- Вывести список студентов, имеющих одинаковые имена, но не совпадающие ид.
SELECT "П1"."ИМЯ", "П1"."ИД", "П2"."ИД"
FROM "Н_ЛЮДИ" AS "П1"
JOIN "Н_ЛЮДИ" AS "П2" ON "П1"."ИМЯ" = "П2"."ИМЯ"
WHERE "П1"."ИД" < "П2"."ИД";
